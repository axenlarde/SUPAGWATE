package com.alex.supagwate.office;

/**********************************
 * Used to store an custom settings
 * 
 * @author RATEL Alexandre
 **********************************/
public class CustomSettings
	{
	/****
	 * Variables
	 */
	private String targetname, value;
	
	/***************
	 * Constructor
	 ***************/
	public CustomSettings(String targetname, String value)
		{
		super();
		this.targetname = targetname;
		this.value = value;
		}

	public String getTargetname()
		{
		return targetname;
		}

	public String getValue()
		{
		return value;
		}
	
	/*2019*//*RATEL Alexandre 8)*/
	}

